                                            <v-text-field v-model="editedItem.protein" label="Protein (g)">
                                            </v-text-field>
                                        </v-col>
                                    </v-row>
                                </v-container>
                            </v-card-text>

                            <v-card-actions>
                                <v-spacer></v-spacer>
                                <v-btn color="blue darken-1" text @click="close">Cancel</v-btn>
                                <v-btn color="blue darken-1" text @click="save">Save</v-btn>
                            </v-card-actions>
                        </v-card>
                    </v-dialog>
                </v-toolbar>
            </template>
            <template v-slot:item.actions="{ item }">
                <v-icon class="mr-3" @click="editItem(item)">
                   mdi-arrow-up
                </v-icon>
                <v-icon class="mr-3" @click="deleteItem(item)">
                   mdi-arrow-down
                </v-icon>
                <v-icon class="mr-3" @click="deleteItem(item)">
                   mdi-close
                </v-icon>
                <v-icon  @click="editItem(item)">
                    mdi-play-circle-outline
                </v-icon>
            </template>
            <template v-slot:no-data>
                <v-btn color="primary" @click="initialize">Reset</v-btn>
            </template>
        </v-data-table>
    </v-container>
</template>


<script>
    export default {

        data: () => ({
            dialog: false,
            headers: [{
                    text: '检测项',
                    align: 'start',
                    sortable: false,
                    value: 'name',
                },
                {
                    text: '操作',
                    value: 'actions',
                    sortable: false
                },
            ],
            desserts: [],
            editedIndex: -1,
            editedItem: {
                name: '',
                calories: 0,
                fat: 0,
                carbs: 0,
                protein: 0,
            },
            defaultItem: {
                name: '',
                calories: 0,
                fat: 0,
                carbs: 0,
                protein: 0,
            },
        }),

        computed: {
            formTitle() {
                return this.editedIndex === -1 ? 'New Item' : 'Edit Item'
            },
        },

        watch: {
            dialog(val) {
                val || this.close()
            },
        },

        created() {
            this.initialize()
        },

        methods: {
            initialize() {
                this.desserts = [{
                        name: '测试项1',
                        calories: 159,
